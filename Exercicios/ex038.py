num1 = int(input('Digite um número inteiro:'))
num2 = int(input('Digiite outro número inteiro:'))
if num1 > num2:
    print(f'Seu primeiro número, {num1}, é maior que o segundo, {num2}!')
elif num1 < num2:
    print(f'Seu segundo número, {num2}, é maior que o primeiro, {num1}!')
else:
    print(f'Os números escolhidos SÃO IGUAIS!')
